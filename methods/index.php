<h2>Международная система уровней</h2>
<p>Вы можете пройти бесплатное тестирование для определения своего уровня и сразу же начать обучение в подходящей для вас группе. Ваши знания иностранного языка будут определены по принятой IH «Общеевропейской системе уровней» (CEFR), состоящей из 6 ступеней. Эта шкала определяет знание языка от низшего уровня А1 (минимальный) до самого высокого С2 Mastery (владениe в совершенстве). «Международная шкала уровней» в соответствии со ступенями CEFR выглядит так: самый низкий уровень - <strong>Beginner, A1 - Elementary, A2 - Pre-Intermediate, B1 - Intermediate, B2 - Upper-Intermediate, С1 - Advanced, С2 - Proficiency</strong>.</p>
<p>По прохождении каждой ступени студенты сдают тест для определения результатов обучения. Он включает проверку навыков чтения, устной и письменной речи, восприятия на слух.</p>

